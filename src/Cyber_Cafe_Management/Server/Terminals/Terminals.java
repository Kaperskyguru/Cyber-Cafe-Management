    private void displayTerminals() {
        Vector<Vector<String>> data1 = new Vector<>();
        try {
            Connection con = DatabaseClass.Connection();
            Statement st = con.createStatement(ResultSet.CONCUR_UPDATABLE, ResultSet.TYPE_SCROLL_INSENSITIVE);
            ResultSet rs = st.executeQuery("select * from Terminaltable");
            while (rs.next()) {
                Vector<String> data = new Vector<>();
                data.add(rs.getInt("Terminal_ID") + "");
                data.add(rs.getString("Terminal_Name"));
                data.add(rs.getInt("Terminal_Number") + "");
                data.add(rs.getString("Terminal_IpAddress"));
                data.add(rs.getInt("Terminal_Port") + "");
                data1.add(data);
            }
        } catch (SQLException e) {
            e.printStackTrace();

        }

        Vector<String> header = new Vector<>();
        header.add("ID");
        header.add("Name");
        header.add("Computer Name");
        header.add("IP Address");
        header.add("Profile");
        header.add("Username");
        header.add("Port");

        jTable2.setModel(new DefaultTableModel(data1, header));
    }
